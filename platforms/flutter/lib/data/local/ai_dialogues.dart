  ///
  /// [sourceId] 說話者 ID
  /// [targetId] 目標角色 ID
  /// [random] 隨機數生成器
  static String? getTargetedDialogue(
    String sourceId,
    String targetId,
    Random random,
  ) {
    final dialogues = _targetedDialogueMap[sourceId]?[targetId];
    if (dialogues == null || dialogues.isEmpty) {
      return null;
    }
    return dialogues[random.nextInt(dialogues.length)];
