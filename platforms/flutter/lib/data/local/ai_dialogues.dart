  AIDialogues._();

  static final Random _random = Random();

  /// 獲取角色台詞
  ///
  /// [roleId] 角色 ID
  /// [context] 情境
  /// [targetRoleId] 目標角色 ID（可選，用於針對性台詞）
  static String getDialogue(
    String roleId,
    DialogueContext context, {
    String? targetRoleId,
  }) {
    final dialogues = _getDialoguesForRole(roleId, context, targetRoleId);
    if (dialogues.isEmpty) {
      return _getDefaultDialogue(context);
    }
    return dialogues[_random.nextInt(dialogues.length)];
  }

  /// 獲取角色的所有台詞
  static List<String> getAllDialogues(String roleId, DialogueContext context) {
    return _getDialoguesForRole(roleId, context, null);
  }

  /// 根據角色獲取台詞列表
