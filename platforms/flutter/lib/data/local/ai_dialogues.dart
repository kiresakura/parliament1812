// 台詞符合 1812 年英國工業革命時期的歷史背景

import 'dart:math';

/// 台詞情境類型
enum DialogueContext {
  /// 開場白
  greeting,

  /// 發起攻擊（質詢）
  attacking,

  /// 防禦（反駁）
  defending,

  /// 被攻擊時
  beingAttacked,

  /// 請求結盟
  allyRequest,

  /// 接受結盟
  allyAccept,

  /// 拒絕結盟
  allyReject,

  /// 背叛時
  betraying,

  /// 使用技能
  usingSkill,

  /// 投票時
  voting,

  /// 獲勝
  winning,

  /// 落敗
  losing,

  /// 聲望過低
  lowHealth,

  /// 嘲諷
  taunt,
}

/// 對話目標類型
enum DialogueTarget {
  /// 通用（任何人）
  general,

  /// 針對工人
  toWorker,

  /// 針對資方
  toFactory,

  /// 針對記者
  toPress,

  /// 針對盧德派
  toLuddite,
}

/// 角色台詞資料庫
