/// 對話修飾器
class DialogueModifiers {
  DialogueModifiers._();

  /// 根據聲望添加情緒前綴
  ///
  /// [dialogue] 原始對話
  /// [reputation] 當前聲望值
  static String addEmotionalPrefix(String dialogue, int reputation) {
    if (reputation <= 20) {
      // 聲望極低時的絕望前綴
      final prefixes = [
        '（喘息著）',
        '（虛弱地）',
        '（顫抖著）',
        '（勉強站穩）',
        '（咳嗽）',
      ];
      return '${prefixes[reputation % prefixes.length]}$dialogue';
    } else if (reputation <= 40) {
      // 聲望較低時的緊張前綴
      final prefixes = [
        '（緊張地）',
        '（不安地）',
        '（皺眉）',
        '（深呼吸）',
      ];
      return '${prefixes[reputation % prefixes.length]}$dialogue';
    } else if (reputation >= 80) {
      // 聲望極高時的自信前綴
      final prefixes = [
        '（自信地）',
        '（昂首挺胸）',
        '（得意地）',
        '（從容不迫）',
      ];
      return '${prefixes[reputation % prefixes.length]}$dialogue';
    }
    // 正常聲望不添加前綴
    return dialogue;
  }

  /// 根據情境添加動作描述
  ///
  /// [dialogue] 原始對話
  /// [context] 對話情境
  static String addActionDescription(
    String dialogue,
    DialogueContext context,
  ) {
    switch (context) {
      case DialogueContext.attacking:
        final actions = [
          '（指著對方）',
          '（站起身來）',
          '（拍桌子）',
          '（向前一步）',
        ];
        return '${actions[dialogue.length % actions.length]}$dialogue';

      case DialogueContext.defending:
        final actions = [
          '（挺直身體）',
          '（搖頭）',
          '（冷笑）',
          '（攤開雙手）',
        ];
        return '${actions[dialogue.length % actions.length]}$dialogue';

      case DialogueContext.beingAttacked:
        final actions = [
          '（皺眉）',
          '（退後一步）',
          '（握緊拳頭）',
          '（深吸一口氣）',
        ];
        return '${actions[dialogue.length % actions.length]}$dialogue';

      case DialogueContext.winning:
        final actions = [
          '（微笑）',
          '（點頭）',
          '（揮手致意）',
          '（鬆了口氣）',
        ];
        return '${actions[dialogue.length % actions.length]}$dialogue';

      case DialogueContext.losing:
        final actions = [
          '（低下頭）',
          '（嘆氣）',
          '（閉上眼睛）',
          '（轉身）',
        ];
        return '${actions[dialogue.length % actions.length]}$dialogue';

      case DialogueContext.taunt:
        final actions = [
          '（嘲諷地）',
          '（冷笑）',
          '（翻白眼）',
          '（輕蔑地）',
        ];
        return '${actions[dialogue.length % actions.length]}$dialogue';

      default:
        // 其他情境不添加動作描述
        return dialogue;
    }
  }
}
